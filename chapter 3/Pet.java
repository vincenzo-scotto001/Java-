public class Pet
{
   private String name;
   private String animal;
   private double age;
   
   public void setName(String n)
      {
         name = n;
      }
   public void setAnimal(String a)
      {
         animal = a;
      }
   public void setAge(double ag)
      {
         age = ag;
      }
   public String getName()
      {
         return name;
      }
   public String getAnimal()
      {
         return animal;
      }
   public double getAge()
      {
         return age;
      }
}
   
