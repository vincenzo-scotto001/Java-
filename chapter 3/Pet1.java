public class Pet1
{
   private String name;
   private String type;
   private int age;
   
   public Pet1()
   {
      name = "";
      type = "";
      age = 0;
    }
    
    public void setName(String name)
    {
      name = name;
    }
    
    public void setType(String type)
    {
      type = type;
    }
    
    public void setAge(int age)
    {
      age = age;
    }
    
    public String getName()
    {
      return name;
    }
    public String getType()
    {
      return type;
    }
    
    public int getAge()
    {
      return age;
    }
    
}